     */
    public function store(Request $request)
    {
        //validate the input
        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'class' => 'required',
            'class_roll' => 'required',
            'detail' => 'required',
            'detail' => 'required',
        ]);

        //creat a new product
        Student::create($request->all());

        //redirect the user and send friendly message
        return redirect()->route('students.create')->with('success','Student added successfully');
    }

